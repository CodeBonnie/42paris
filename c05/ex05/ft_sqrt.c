int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 0)
		return (0);
	i = 0;
	while (i <= nb / 2 && i < 46341)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	if (i * i == nb)
		return (i);
	return (0);
}
